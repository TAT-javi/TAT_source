
void pio_wr_msg( char*, int, int);
/*REGISTER FOR CONTROL C0'*/
#define MAINPWRREG	0x01	/*0000 0001 */

/*REGISTER FOR CONTROL C2*/
#define VDCPWRREG	0x04	/*0000 0100*/


/*REGISTER FOR DATA	D0-7*/
#define RAPWRREG	0x01	/*0000 0001*/
#define DECPWRREG	0x02	/*0000 0010*/
#define ROTPWRREG	0x04	/*0000 0100*/
#define TRANPWRREG	0x08	/*0000 1000*/

#define CCDPWRREG	0x10	/*0001 0000*/
#define WHEELPWRREG	0x20	/*0010 0000*/
#define DSPPWRREG	0x40	/*0100 0000*/
#define DSPJPREG	0x80	/*1000 0000*/

/*REGISTER FOR STATUS S3-7
				   7654 3210 */
#define RAEASTREG	0x40	/* 0100 0000 S6*/
#define RAWESTREG	0x80	/* 1000 0000 S7'*/
#define DECSOUTHREG	0x20	/* 0010 0000 S5*/
#define DECNORTHREG	0x10	/* 0001 0000 S4*/
#define RADECLIMREG	0x08	/* 0000 1000 S3*/
